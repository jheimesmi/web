				<b>Condiciones de las mismas:</b>
					- Todas las reglas son y serán públicas.
					- Cada regla tendrá un númerio identificatorio.
					- Los únicos capaces de crear una nueva regla son los administradores generales.
					- GranRol &copy; al ser una comunidad que promueve la participación de los usuarios, fomenta la implicación de estos en las reglas haciendo posible que un usuario promueva la creación de nuevas reglas para favorecer el rol.(+Info)
					- Toda regla será mostrada aquí y en el foro, las reglas no se pueden crear IC. En razones de fuerza mayor donde un usuario o varios abusen de algo que no está escrito aquí se les puede prohibir que cometan la acción dicha hasta que se aclare el problema a través del foro.
					<br>
				<b>Servidor</b>
					Nº 1: El uso de trucos está prohibido.
					Cualquier programa que desestabilice el juego lo altere para beneficio propio (o no) queda terminalmente prohibido. El uso de estos acarrea la expulsión indeterminada de la comunidad.
					Nº 2: Prohibido el Powergame.
					Aquel jugador que realice acciones irrealistas (Por ejemplo, saltar de una gran altura y no resultar herido) o fuerze el rol de otro jugador será sancionado.
					Nº 3: Prohibido el Metagame.
					Aquel jugador que consigue información OOC y la usa IC. (Por ejemplo, saber IC que otro jugador es el dueño de una mafia porque te lo dijo por mensaje privado).
					Nº 4: Prohibido el Deathmach.
					Aquel jugador que golpea a alguien sin fundamentos.
					Nº 5: Prohibido el CarKill.
					Matar a otros jugadores desde cualquier vehículo (helicoptero, coche, avión, etc.) sin motivos.
					Nº 6: Prohibido el Salto de Conejo (Bunny Jump).
					Ir saltando para llegar más rápido a un lugar.
					Nº 7: Prohibido el RevengeKill.
					El usuario vuelve a un conflicto después de su muerte. Cuando una persona muere pero no quiere CK, deberá rolear ser atentido en el hospital por heridas y no podrá volver al combate después de 30 minutos.
					Nº 8: Abuso de Errores.
					Siempre se nos puede escapar algún error(bug), abusar de estos puede conllevar a un baneo. Un usuario que conoce de algún error está obligado a comunicarlo a la administración.
					<br>
				<b>Servidor - Cuentas</b>
					Nº 9: Nombres.
					Aquellos nombres que sean famosos (Por ejemplo, Will Smith) quedan prohibidos así como también aquellos donde se usen diminutivos (Por ejemplo, Juansito Paredes) y finalmente los que el nombre y apellido den como resultado una vulgaridad. Se aceptan nombres con 3 mayúsculas (Por ejemplo, John McAllister).
					Nº 10: Ventas.
					No se pueden vender cuentas, cada una es única y solo pertenece a un solo jugador. Tampoco se pueden comprar artículos OOC con dinero IC.
					Nº 11: Skins.
					No hay skins de facciones especificas. Todos los skin pueden ser usados por cualquier jugador. Los skins de facciones legales (LSPD, LSMD, FBI) se pueden conseguir mediante el mercado negro.
					Nº 12: Roles Especiales.
					Aquellas personas o grupos de personas que desean realizar roles poco comunes y que necesiten un buen conocimiento del rol (Por ejemplo, asaltante, psicópta, asesino a sueldo, etc) deberán pedir una autorización a un administrador.
					<br>
				<b>Servidor - Comunicación</b>
					Nº 13: Confusión de canales.
					No se puede confundir un canal IC con uno OOC en reiteradas ocasiones.
					Nº 14: Transmisión de mensajes.
					Queda prohibido la tranmisión de mensajes IC a través de canales OOC, esto será considerado Metagame.
					Nº 15: Texto.
					A la hora de comunicarse, no se podrá usar un dialecto SMS (ola kmo sts?) ni dialectos vulgares que representen a personas carentes de inteligencia (SáArp4d0 EnN GáÁt0).
					Nº 16: Mayúsculas.
					Queda prohibido (en canales OOC) el uso de mayúsculas constantemente.
					Nº 17: Ortografía.
					Se debe tener una ortografía entendible, en lo posible con el uso de tildes, comas y puntos. 
					Nº 18: Insultos.
					No se puede insultar fuera del juego a ningún usuario de la comunidad.
					Nº 19: Idioma.
					Se rolea que estamos en Estados Unidos por ende se habla el Inglés aunque se escribe en Castellano. No se puede escribir en otros idiomas. IC se pueden usar algunas oraciones en Inglés.
					Nº 20: Reportes.
					Todos los reportes de rol irán al foro.
					Nº 21: Mal uso de los reportes.
					El comando /reportar sirve para informar de alguna situación que se está viviendo en el servidor, alguien usando trucos, insultando etc. No para enviar comentarios o dudas.
					Nº 22: Reportes por MP.
					Queda prohibido reportar algo por MP (Mensaje Privado).
					Nº 23: Dudas.
					Cualquier duda debe ser expresada mediante el canal /duda.
					Nº 24: Comentarios.
					Nos alegra saber que muchos usuarios tienen ideas, comentarios y quejas sobre diversos ámbitos del servidor por lo tanto todos estos comentarios deben ser escritos en el foro, no mediante el servidor.
					Nº 25: Mensajes Privados.
					Mensajes privados insistentes a los administradores pueden ser sancionados.
					<br>
				<b>Servidor - Pausas</b>
					Nº 26: AFK.
					No se puede estar inactivo (AFK) en lugares públicos, solo en lugares ocultos pero no durante mucho tiempo, existen exepciones justificadas (Atender el telefono, ir al baño) para estar AFK pero antes se debe avisar a un administrador mediante el comando "/?? AFK Atiendo el telefono".
					Nº 27: LAG.
					La creación de lag mediante los bugs del GTA San Andreas no se puede permitir y hacer esto conlleva un baneo de dos semanas.
					Nº 28: Quit.
					Desconectarse en medio de un rol para evitar (o no) roles que pueden perjudicarte, es sancionable.
					<br>
				<b>Servidor - Muertes</b>
					Nº 29: Muerte parcial (PK)
					En este servidor la muerte parcial no existe, solo la muerte completa del personaje. (+Info)
					Nº 30: Enfrentamientos armados.
					Los ataques y enfrentamientos entre bandas y mafias por diversos motivos (venganza, territorios, etc) serán regulados por un administrador. Hay dos tipos de enfrentamientos, comunes y especiales. Los comunes tienen victimas de disparos con pocas muertes o ninguna y los especiales tendrán victimas y estos pueden llevar al fin de una banda. Para un enfrentamiento especial se pide un permiso a un administrador. (+Info)
					Nº 31: Muerte completa (CK).
					Las muertes completas significan perder el 25% del dinero de todo el personaje e incluye un cambio de nombre gratuito. Como máximo se puede morir 1 vez al mes, de morir más deberá abonar un cambio de nombre o esperar a que se cumpla el mes. Las muertes deben ser aprobadas entre el asesino/s y el personaje muerto y publicar una petición de CK en el foro. Sí el muerto no quiere aceptar un CK, el/los asesino/s pueden hacer la petición de CK de todas formas; un administrador solucionará la disputa.
					Nº 32: Lideres Legales.
					No se puede matar a un lider legal* (LSPD, LSMD, FBI, Alcaldia) o a un alto mando (Rango +4) sin buenos fundamentos. Un administrador root deberá autorizar el INTENTO de asesinato que será totalmente confidencial. Si el CK es aceptado la victima sufrirá un descenso en su rango.
					Nº 33: Degollar.
					No se puede degollar con un cuchillo a una persona a menos que se lleve a cabo un rol especial y se tengan fundamentos.
					Nº 34: Partidos Políticos.
					Mientras partidos políticos se disputan el poder, ninguno de estos podrá sufrir atentados a menos que se tengan fundamentos razonables (que no sean de eliminar la competencia) un administrador podrá autorizar el rol. 
				<b>Servidor - El rol</b>
					Nº 35: Armas.
					Toda arma pesada debe ser roleada con el /me indicando de donde se saca. En el caso de las armas pequeñas, no es preciso rolearlo.
					Nº 36: Peleas.
					No es necesario rolear un golpe a otra persona a menos que el golpe intente noquearlo. Si un jugador desea golpear a otro puede hacerlo sin el /me, luego de tres golpes este debe esperar dos segundos antes de acertar otros tres golpes. NO es necesario golpearlo hasta que el jugador quede sin vida, sino hasta que este quede debilitado, la victima debe rolear quedar noqueada o estar completamente debilitada e incapaz de defenderse.
					Nº 37: Entorno.
					El entorno siempre debe ser roleado, no podemos conducir como locos en la ciudad sin motivos. Siempre se supone que hay gente por las calles, policías, civiles, etc. (+Info)
					Nº 38: Desbug.
					Confiamos en nuestros usuarios, por eso hay un comando para desbuguearse sin ayuda de un administrador. Es importante destacar que este comando solamente puede ser utilizado cuando NO se está roleando. En caso de estar bugueado en un rol, el jugador deberá pedir desbug a un administrador.
